#include <stdio.h>
int add(int a, int b)
{
	int sum = 0;

	sum = a + b;
	return (sum);
}
int sub(int a, int b)
{
        int result = 0;

        result = a - b;
	return (result);
}
int mul(int a, int b)
{
        int result = 0;

        result = a * b;
	return (result);
}
int div(int a, int b)
{
        int result = 0;

        result = a / b;
	return (result);
}
int mod(int a, int b)
{
        int result = 0;

        result = a % b;
	return (result);
}

#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - add sum of 2 numbers
 * @a: first number
 * @b: second number
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction of 2 numbers
 * @a: first number
 * @b: second number
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication of 2 numbers
 * @a: first number
 * @b: second number
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of 2 numbers
 * @a: first number
 * @b: second number
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modules of a number numbers
 * @a: first number
 * @b: second number
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}	
